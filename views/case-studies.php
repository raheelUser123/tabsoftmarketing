                                <h3>Step 1: Make all Websites on WooCommerce (WordPress)</h3>
                                <p>
                                    The WooCommerce platform was chosen because of the flexibility and easy deployment it has to offer. It allowed us to easily manage CMS for all the websites, only changing the design and localisation for different geolocations.
                                </p>
                            </div>
                        </li>
                        <li>
                            <span>2</span>
                            <div class="strategic-li">
                                <h3>Step 2: Gather Data in One Place for Transparency and Insights</h3>
                                <p>
                                    The next step involved collecting all the business and sales data from analytics plugins, Google Analytics, and custom dashboards in one place in the admin panel to increase clarity on buying patterns and weekly updates on sales.
                                </p>
                            </div>
                        </li>
                        <li>
                            <span>3</span>
                            <div class="strategic-li">
                                <h3>Step 3: Deployment of the Websites in 2 Months</h3>
                                <p>
                                    We also deployed the websites of Spanish and Portuguese eCommerce stores in just 2 months, where we added the first version of products and new features. We also implemented a continuously improving product catalogue with every new release each month to improve the UX and conversions.
                                </p>
                            </div>
                        </li>
                        <li>
                            <span>4</span>
                            <div class="strategic-li">
                                <h3>Step 4: Set Up Search Engine Optimisation for Free Organic Traffic</h3>
                                <p>
                                    Once the websites migrated to the new platform, the next step was to set up proper SEO. Our first priority was detailed traffic research, including searching for competitors' gaps in popular product categories.
                                    This research allowed us to create an ideal website structure and optimise all best-selling categories and top products. This way, we were able to provide the best hybrid (UX and brick-and-mortar) experience to the brand’s clients.
                                </p>
                            </div>
                        </li>
                        <li>
                            <span>5</span>
                            <div class="strategic-li">
                                <h3>Step 5: Improve Website’s Search and Customer Experience</h3>
                                <p>
                                    We also worked on improving the website's overall structure and search by adding filters, proper product tags, category and product descriptions, and improving the internal SILO structure with product searches.
                                </p>
                            </div>
                        </li>
                        <li>
                            <span>6</span>
                            <div class="strategic-li">
                                <h3>Step 6: Upload and Manage Product Catalogue with no Development Help</h3>
                                <p>
                                    We uploaded a massive product catalogue (50k+ products) using the existing product specs. We also set up a system to manage these catalogues without development help, allowing tech scripts to automatically upload products so different local teams can publish them, improving all specs, descriptions, prices and models on their websites.
                                </p>
                            </div>
                        </li>
                        <li>
                            <span>7</span>
                            <div class="strategic-li">
                                <h3>Step 7: Collect and Localise Verified Reviews</h3>
                                <p>
                                    We collected reviews from 8 countries, localised them, and uploaded all of them to all the company’s websites to grow customer loyalty and trust, and improve the social proof of all product pages and categories.
                                </p>
                            </div>
